subroutine handle_external_charges()
      use comafnmr
      implicit none
      double precision :: a,b,c,d
      integer :: iitemp,j,kk,nprotc,nsf

!       Deal with the charges representing the protein and solvent polarization:

        if( gaussian ) then
          write(30,*)
        else if ( qchem ) then
          write(30,'(a)') '$end'
          write(30,'(a)') ''
          write(30,'(a)') '$external_charges'
        else if ( jaguar ) then
          write(30,'(a)') '&'
          write(30,'(a)') '&pointchg'
        else if ( demon ) then
          write(30,'(a)') 'END'
          write(30,'(a)') 'EMBED READ'
        else if ( sqm ) then
          write(30,'(a)') '#EXCHARGES'
        endif

        if( quick ) write(44,*)

!       write out the protein positions and charges:

        do kk=1,natom
          if( .not. atomsign(kk) ) then
            if ( restype(resno(kk)).ne.'W' ) &
            write(33,'(a,i5,1x,a4,1x,a3,i6,4x,3f8.3,f8.4,f8.3,6x,a2)') &
               'ATOM  ', kk,atomname(kk),residue(kk),resno_user(kk), &
               (coord(j,kk),j=1,3), qmcharge(kk),rad(kk),element(kk)
          endif
        enddo

        close(33)
        call execute_command_line('./runsolinprot ' // filek, &
             exitstat = ier)
        if( ier .ne. 0 )then
           write(0,*) "error in solinprot: check solinprot.out"
           write(0,*) "error code was ", ier
           write(0,*) "command line was:"
           write(0,*) './runsolinprot ' // filek
           stop 1
        end if

        if( orca ) then
          nsf=0  ! number of surface points, just count them here
          open(23,file='srfchg.pos')
          do iitemp=1,999999
            read(23,*,end=59)a,b,c,d
            nsf = nsf + 1
          enddo
59        continue
          close(23)
          write(32,'(i7)') nsf
        endif

        open(23,file='srfchg.pos')
        do iitemp=1,999999
          read(23,*,end=60)a,b,c,d
          if( gaussian .or. qchem .or. demon ) then
            write(30,'(3f15.6,2x,f12.8)')a,b,c,d
          else if( jaguar ) then
            write(30,'(f12.8,2x,3f10.4)')d,a,b,c
          else if( orca .or. terachem ) then
            write(32,'(f12.8,2x,3f15.6)')d,a,b,c
          else if ( sqm ) then
            !  write out surface charge distributions, for visualization
            ! write(35,'(a,i5,1x,a4,1x,a3,i6,4x,3f8.3,f8.4,f8.2,6x,a2)') &
            !  'ATOM  ', iitemp,' H  ','SRF',1, a,b,c,d, 1.0, ' H'
            write(30, '(a,3f15.6,2x,f12.8)') ' 1  H ', a,b,c,d
          endif
          if( quick ) write(44,'(3f15.6,2x,f12.8)')a,b,c,d
        enddo
60      continue
        close(23)
        return
end subroutine handle_external_charges

