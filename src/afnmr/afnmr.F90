module comafnmr

!  some global variables go here

   implicit none
   integer,parameter::MAXAT=60000,MAXRES=20000,MAXIQM=600
   double precision::coord(3,MAXAT), fxyz(3,MAXIQM)
   double precision:: qmcharge(MAXAT),rad(MAXAT)
   logical::connect(MAXRES,MAXRES)
   logical::atomsign(MAXAT)
   logical::ter(0:MAXRES)
   character(len=1):: restype(MAXRES)
   character(len=2):: element(MAXAT),dummyl
   character(len=3):: residue(MAXAT), residuename(MAXRES)
   character(len=4):: atomname(MAXAT), atomname_uniq(MAXAT), tmpnam
   character(len=5):: dlabel(MAXAT)
   character(len=6):: cfragtxt
   character(len=160):: commandline, line, afnmrhome
   character(len=30) :: pqrstart, atombasis(MAXIQM)
   character(len=24) :: pqrend
   character(len=5) functional
   character(len=:), allocatable :: filek, version
   character(len=1) :: basis
!     here resno() goes sequentially from 1 to nres;
!          resno_user() are the residue numbers in the input pdb file
!          (note: does not recognize chainID's, so all resno_user() values
!           should be unique)
   integer ::resno(MAXAT), resno_user(MAXAT), list(MAXRES), prev_resno_user
   integer :: modnum, ier, listsize, lengthb, natom, nhighatom, nlowatom
   integer :: firstprotres, lastprotres, lastprotatom, iatfinal, iatstart
   logical :: gaussian, orca, demon, demon5, qchem, jaguar, terachem, &
              qopt, xtb, sqm, quick, spinspin
   logical :: first=.true.
   logical :: nofrag=.false.
   double precision :: nbcut

end module

program afnmr_x

!   (Note: generally the input for afnmr.x is created by the shell script
!      "afnmr", which is in the AFNMRHOME/bin directory.)
!
!   Usage: afnmr.x program basis qopt functional nbcut spinspin basename list
!
!      where program  is G (Gaussian), O (Orca), D (Demon v3,4), E (Demon v5),
!                     Q (Qchem), J (Jaguar), or S (sqm)
!            basis is D (double-zeta) or T (triple-zeta) 
!                  or M (primary res. Q, rest T), A (aug-tzp), or S (shape)
!            qopt controls off quantum geometry optimization: set to
!                  E/G/O/X/Q/T (for optimization with demon, Gaussian, ORCA, xtb, 
!                  quick or terachem), or use F (false, default) to turn off
!            functional is a string giving the desired DFT functional
!            nbcut is the heavy-atom nonbonded cutoff for fragment creation
!            spinspin is T for J-couplings, F (default) otherwise
!            <basename>.pqr gives the input structure; put all "general"
!                  residues (water, ligands, etc.) after protein or
!                  nucleic acid residues
!            list is a list of residues to fragment; if empty, do all
!                residues up to lastprotres
!
      use comafnmr
      implicit none

      double precision :: x,y,z,tempdis
      character(len=8) :: lpchar
      character(len=1) :: program,qoptb,spinspinb
      integer :: selectC(0:MAXRES+2),charge(MAXRES),cfrag
      integer :: selectCA(MAXRES+2), resmap(MAXRES)
      integer :: i,j,k,kk,m,iitemp,iqm,iqmprot=0
      integer :: kfinal,ktemp,kstart,kcount,kbas,kuser
      integer :: n1,n2,nprotc,nres,nsf,nptemp
      character(len=3) :: rn, kuser_c
      character(len=6) :: nbcutb

      double precision dis
      double precision chargef(MAXRES)
      character(len=80) basename
      character(len=:), allocatable :: pdbfile
      integer lengthc,iline
#ifdef __INTEL_COMPILER
      integer system
#endif

      integer, parameter ::MAXNRES=33,MAXPRES=43
      character(len=3) :: nresn(MAXNRES), presn(MAXPRES)

      nresn(1) = '  G'
      nresn(2) = '  A'
      nresn(3) = '  U'
      nresn(4) = '  C'
      nresn(5) = ' DG'
      nresn(6) = ' DA'
      nresn(7) = ' DT'
      nresn(8) = ' DC'
      nresn(9) = 'GUA'
      nresn(10) = 'ADE'
      nresn(11) = 'THY'
      nresn(12) = 'CYT'
      nresn(13) = 'URA'
      nresn(14) = ' G5'
      nresn(15) = ' A5'
      nresn(16) = ' U5'
      nresn(17) = ' C5'
      nresn(18) = 'DG5'
      nresn(19) = 'DA5'
      nresn(20) = 'DT5'
      nresn(21) = 'DC5'
      nresn(22) = ' G3'
      nresn(23) = ' A3'
      nresn(24) = ' U3'
      nresn(25) = ' C3'
      nresn(26) = 'DG3'
      nresn(27) = 'DA3'
      nresn(28) = 'DT3'
      nresn(29) = 'DC3'
      nresn(30) = 'gtp'
      nresn(31) = 'DMA'
      nresn(32) = 'M6A'
      nresn(33) = 'CYH'

      presn(1) = 'ALA'
      presn(2) = 'ARG'
      presn(3) = 'ASH'
      presn(4) = 'ASN'
      presn(5) = 'ASP'
      presn(6) = 'CYS'
      presn(7) = 'CYX'
      presn(8) = 'GLH'
      presn(9) = 'GLN'
      presn(10) = 'GLU'
      presn(11) = 'GLY'
      presn(12) = 'HID'
      presn(13) = 'HIE'
      presn(14) = 'HIP'
      presn(15) = 'ILE'
      presn(16) = 'LEU'
      presn(17) = 'LYN'
      presn(18) = 'LYS'
      presn(19) = 'MET'
      presn(20) = 'PHE'
      presn(21) = 'PRO'
      presn(22) = 'SER'
      presn(23) = 'THR'
      presn(24) = 'TRP'
      presn(25) = 'TYR'
      presn(26) = 'VAL'
      presn(27) = 'HIS'
      presn(28) = 'ACE'
      presn(29) = 'NHE'
      presn(30) = 'NME'
      presn(31) = 'CYB'
      presn(32) = 'PTR'
      presn(33) = 'SEP'
      presn(34) = 'TPO'
      presn(35) = 'Y1P'
      presn(36) = 'S1P'
      presn(37) = 'T1P'
      presn(38) = 'H1D'
      presn(39) = 'H2D'
      presn(40) = 'H1E'
      presn(41) = 'H2E'
      presn(42) = 'EEY'
      presn(43) = 'BTF'

      gaussian = .false.
      orca = .false.
      qchem = .false.
      demon = .false.
      demon5 = .false.
      terachem = .false.
      jaguar = .false.
      xtb = .false.
      sqm = .false.
      quick = .false.
      qopt = .false.
      spinspin = .false.
      listsize = 0
      version = '1.9'

      write(6,*)
      write(6,*)'**********************************************'
      write(6,*)'AF-NMR'
      write(6,*)
      write(6,*)'Authors : Xiao He, Kenneth Merz,'
      write(6,*)'          Sishi Tang and David A. Case'
      write(6,*)' Version: ', trim(version)
      write(6,*)'**********************************************'

      call get_environment_variable('AFNMRHOME', afnmrhome )

      if( command_argument_count() .lt. 5 ) then
         write(6,*) &
       'Usage: af-nmr program basis qopt <basename> {list}'
         stop 1
      end if

      call get_command_argument( 1, program, lengthb )
      if( program .eq. 'G' ) then
         gaussian = .true.
      else if( program .eq. 'D' ) then
         demon = .true.
      else if( program .eq. 'E' ) then
         demon = .true.
         demon5 = .true.
      else if( program .eq. 'O' ) then
         orca = .true.
      else if( program .eq. 'Q' ) then
         qchem = .true.
      else if( program .eq. 'J' ) then
         jaguar = .true.
      else if( program .eq. 'S' ) then
         sqm = .true.
      else
         write(0,*) 'Bad input for program: ', program
         stop 1
      endif

      call get_command_argument( 2, basis, lengthb )

      call get_command_argument( 3, qoptb, lengthb )
      if( qoptb .eq. 'D' .or. qoptb .eq. 'E' .or. qoptb .eq. 'G' &
                         .or. qoptb .eq.  'O' ) then
         qopt = .true.
      else if (qoptb .eq. 'X' ) then
         xtb = .true.
      else if (qoptb .eq. 'Q' ) then
         quick = .true.
      else if (qoptb .eq. 'T' ) then
         terachem = .true.
      else if (qoptb .ne. 'F' ) then
         write(0,*) 'Bad input for qopt: ', qoptb
         stop 1
      endif

      call get_command_argument( 4, functional, lengthb )

      call get_command_argument( 5, nbcutb, lengthb )
      read( nbcutb, * ) nbcut

      call get_command_argument( 6, spinspinb, lengthb )
      if( spinspinb .eq. 'T' ) spinspin = .true.

      call get_command_argument( 7, basename, lengthb )
      pdbfile = basename(1:lengthb) // '.pqr'

      if( command_argument_count() > 7 ) then
         do i=8,command_argument_count()
            call get_command_argument( i, lpchar, lengthc )
            read( lpchar, '(i4)' ) list(i-7)
         end do
         listsize = command_argument_count() - 7
      endif
      if( listsize .eq. 1 .and. list(1) .eq. 0 ) then 
         nofrag = .true.
         write(6,'(a)') 'Setting nofrag to .true.; fragmentation skipped'
      end if

      write(6,'(a,a)') 'Running afnmr, version ',trim(version)
      write(6,'(a,a1,1x,a1,1x,a1,1x,a,1x,f6.2,1x,a,1x,a)') &
           'Input arguments: ', program, basis, qoptb, &
           functional, nbcut, spinspinb, trim(basename(1:lengthb))
      write(6,'(a,f6.3)') &
         'Fragments will be based on heavy atom contacts < ',nbcut

      ter(0:MAXRES) = .false.
      chargef(1:MAXRES) = 0.d0
!
!     Read the input pqr file:
!
      open(10,file=pdbfile)

      nptemp = 0
      i = 0
      prev_resno_user = 0
      do k=1,MAXAT
        read(10,'(a80)',end = 101) line
        if( line(1:5) .eq. 'ATOM ' .or. line(1:5) .eq. 'HETAT') then
           i = i + 1
           read(line,100)atomname(i),residue(i),  &
             resno_user(i),(coord(j,i),j=1,3),qmcharge(i),rad(i),element(i)
100        format(12x,a4,1x,a3,2x,I4,4x,3f8.3,f8.4,f8.3,6x,a2)
           ! skip extra points:
           if( element(i) .eq. ' E' .or. element(i) .eq. ' ' ) then
              i = i - 1
              cycle
           endif
           ! for nofrag, set all residue numbers to 1; make atom names unique
           if( nofrag ) then
              resno_user(i) = 1
              tmpnam = adjustl(atomname(i))
              write(atomname_uniq(i), '(a,i0.3)') tmpnam(1:1), i
           end if

           if( first ) then
              firstprotres = resno_user(i)
              first = .false.
           endif
           if( resno_user(i) .ne. prev_resno_user ) then ! found new residue
              nptemp = nptemp + 1
              prev_resno_user = resno_user(i)
              !  create list if user didn't enter one:
              if( listsize == 0 ) list(nptemp) = resno_user(i)
           endif
           resno(i) = nptemp
           resmap(resno_user(i)) = nptemp  ! maps user-resno to sequential ones
           chargef(nptemp) = chargef(nptemp) + qmcharge(i)
           if( nptemp.gt.MAXRES ) then
              write(0,*) 'too many residues: ', nptemp, MAXRES
              stop 1
           endif
           residuename(nptemp)=residue(i)

           rn = residue(i)
           restype(nptemp) = 'G'
           do m=1,MAXNRES
             if( rn.eq.nresn(m) ) restype(nptemp) = 'N'
           end do
           do m=1,MAXPRES
             if( rn.eq.presn(m) ) restype(nptemp) = 'P'
           end do
           if( rn.eq.'HOH' .or. rn.eq.'WAT' ) restype(nptemp) = 'W'

        else if( line(1:3) .eq. 'TER' ) then
!          mark residue nptemp as a terminal residue:
           ter(nptemp) = .true.
        endif
      end do
101   natom = i
      nres = max(1,nptemp)

      !  figure out the last protein residue:
      lastprotres = nres
      do i=1,nres
        if( restype(i) .eq. 'G' .or. restype(i) .eq. 'W' ) then
           lastprotres = i-1
           exit
        endif
      end do

      !  figure out last protein atom:
      do i=1,natom
         if( resno(i) .eq. lastprotres ) lastprotatom = i
      end do

!     lastprotres has to be a terminal residue:
      if( lastprotres > 0 ) ter(lastprotres) = .true.

      close(10)

!     real kludge, for pqr files with no element info and no 2-letter elements:
      do i=1,natom
        if( element(i).eq.'  ' ) then
           element(i)(1:1) = ' '
           element(i)(2:2) = atomname(i)(2:2)
        endif
      enddo
!
!     Populate arrays to identify C and CA atoms (for proteins); equivalent
!     atoms (O3' and C3') for nucleotides:
!
      do i=2,natom
        if( restype(resno(i)).eq.'P' ) then
           if (atomname(i).eq.' C  ')  selectC(resno(i))=i
           if (atomname(i).eq.' CA ')  selectCA(resno(i))=i
        else if( restype(resno(i)).eq.'N' ) then
           if (atomname(i).eq.' O3''')  selectC(resno(i))=i
           if (atomname(i).eq.' C3''')  selectCA(resno(i))=i
        else
           if ( resno(i-1).ne.resno(i) ) then
              selectC(resno(i))=i  ! selectC becomes first atom of a gen. res.
              selectC(resno(i-1)+1)=i  ! if residue numbers are not consecutive
           endif
        endif
      enddo
      if( residuename(lastprotres).eq.'NHE' .or. &
          residuename(lastprotres).eq.'NME' ) then
        selectC(lastprotres) = lastprotatom-2  !! will make kfinal=lastprotatom
      endif
      if( restype(1).eq.'G' .or. restype(1) .eq. 'W' ) selectC(1) = 1
      selectC(0) = 1
      selectC(nres+1) = natom+1
!
!     Set up the connectivity arrays:
!
      connect(1:nres,1:nres)=.false.
      do i=1,nres
        connect(i,i)=.true.
      enddo

      charge(1:nres) = nint( chargef(1:nres) )

!----------------------------------------------------------------------------

      do i=1,natom
        if( element(i).eq.' H' ) cycle

        do j=i+1,natom

            if( resno(i).eq.resno(j) ) cycle
            if( element(j).eq.' H' ) cycle
            if( element(j).eq.'Na' ) cycle

            dis=dsqrt((coord(1,i)-coord(1,j))**2   &
             +(coord(2,i)-coord(2,j))**2+(coord(3,i)-coord(3,j))**2)
!
!            nonbond distance < nbcut between heavy atom pairs, or
!                 less than 1.4*nbcut if atom j is a water oxygen:
!
            if( dis.le.nbcut .or. &
                 ( residue(j).eq.'WAT' .and. dis.le.1.4*nbcut ) .or. &
                 ( residue(j).eq.'HOH' .and. dis.le.1.4*nbcut ) ) then 

                connect(resno(i),resno(j))=.true.
                connect(resno(j),resno(i))=.true.
!
!               need to make sure that next residue is also connected if 
!               one of the atoms is beyond selectC:
!
                if( i.ge.selectC(resno(i)) .and. &
                restype(resno(i)).ne.'G' .and. restype(resno(i)).ne.'W') then
                   connect(resno(i)+1,resno(j))=.true.
                   connect(resno(j),resno(i)+1)=.true.
                endif
                if( j.ge.selectC(resno(j)) .and. &
                restype(resno(j)).ne.'G' .and. restype(resno(j)).ne.'W') then
                   connect(resno(i),resno(j)+1)=.true.
                   connect(resno(j)+1,resno(i))=.true.
                endif
            endif

        enddo  !  j=i+1,natom
      enddo    !  i=1,natom
!
!     Big loop over residues to create fragments:
!
      do kcount=1,nres

        if( listsize > 0 ) then
           if( kcount > listsize ) exit ! done with input list
        else
           if( kcount > lastprotres ) exit  ! done with default list
        endif
        ! We need both "kuser" (user's residue number) and "k" (a
        !    sequential residue number:
        kuser = list( kcount )
        if( kuser .eq. 0 ) kuser = 1  ! handle nofrag option
        k = resmap(kuser)

        iqm = 0   !  keep track of the atom number in the output file
        modnum = 0  ! get unique names for added hydrogens

        write(6,*)
        write(6,'(a,i4,a)') '==== Residue ',kuser, &
           ' ==========================================================='
        if( nofrag) then
           filek = basename(1:lengthb)
        else
           write(kuser_c,'(i0.3)') kuser
           filek = basename(1:lengthb)//kuser_c
        end if

        call write_header_info( kuser )

        atomsign(1:natom)=.false. ! flags whether atom is in the quantum region

!       cycle through all "connected" fragments to get the charge on the
!       quantum region (cfrag), and to mark each quantum atom by atomsign(i)

        cfrag = 0
        do ktemp=1,nres
          if(connect(k,ktemp))then
            call get_atom_range( kstart, kfinal, ktemp, &
                   selectC, restype(ktemp), ter)
            atomsign(kstart:kfinal) = .true.
            cfrag = cfrag + charge(ktemp)
            write(6,'(20x,i5,a4,5i6)') kuser,residuename(ktemp), &
                 resno_user(kfinal),kstart,kfinal,charge(ktemp),cfrag
          endif
        enddo
        write(6,'(2i5)') kuser, cfrag
        call write_cfrag_header_info(cfrag)

        nhighatom=0
        nlowatom=0
!
!       get starting, ending atoms for residue "k":
!
        call get_atom_range( iatstart, iatfinal, k, &
            selectC, restype(k), ter )
!
!       write out atoms in principal residue:
!
        do kk=iatstart,iatfinal
          iqm = iqm + 1
          nhighatom = nhighatom + 1
          call addatom( kk, iqm, .true. )
        enddo

        do ktemp=1,nres
          if( ktemp.ne.k .and. connect(k,ktemp) )then

              call get_atom_range( kstart, kfinal, ktemp, &
                   selectC, restype(ktemp), ter)
!
!             write out coordinates in a "connected" residue
!
              do kk=kstart,kfinal
                nlowatom = nlowatom + 1
                iqm = iqm + 1
                call addatom( kk, iqm, .false. )
              enddo
!
!             add hydrogens to dangling residues:
!
              if(ktemp.ne.1 .and. ktemp.le.lastprotres  &
                  .and. .not. ter(ktemp-1) )then
!
!               ---look for "backwards" links to previous residue:
!
                if(.not. atomsign(kstart-1) )then
                  n1=kstart
                  n2=selectCA(ktemp-1)
                  if(atomname(n1).eq.' C  ') then
                    call xyzchange(coord(1,n2),coord(2,n2),coord(3,n2),  &
                      coord(1,n1),coord(2,n1),coord(3,n1),x,y,z)
                  else
                    call xyzchangeO(coord(1,n2),coord(2,n2),coord(3,n2), &
                      coord(1,n1),coord(2,n1),coord(3,n1),x,y,z)
                  endif
                  nlowatom = nlowatom + 1
                  iqm = iqm + 1
                  call addH( iqm, x, y, z )
                endif
              endif

              if(ktemp.lt.lastprotres .and. .not. ter(ktemp)) then
!
!             ---look for "forwards" links to the next residue:
!
                if(.not. atomsign(kfinal+3) )then
                  n1=selectCA(ktemp)
                  n2=selectC(ktemp)
                  call xyzchange(coord(1,n2),coord(2,n2),coord(3,n2),  &
                    coord(1,n1),coord(2,n1),coord(3,n1),x,y,z)
                  nlowatom = nlowatom + 1
                  iqm = iqm + 1
                  call addH( iqm, x, y, z )
                endif
              endif

          endif
          if( ktemp == lastprotres ) iqmprot = iqm  ! only freeze protein
        enddo  ! ktemp

        !  check for dangling S--S bonds:

        do kk=1,natom
          if( atomname(kk)(2:3) == 'SG' .and. atomsign(kk) ) then
!           write(0,*) 'found SG: ', resno(kk), atomsign(kk)
            do i=1,natom
              if( i.eq.kk) cycle
              if( atomname(i)(2:3) .ne. 'SG') cycle
              tempdis=dsqrt((coord(1,i)-coord(1,kk))**2.0d0  &
                 +(coord(2,i)-coord(2,kk))**2.0d0            &
                 +(coord(3,i)-coord(3,kk))**2.0d0)
              if(tempdis.le.2.5d0 .and. kk.ne.i   &
                   .and.  .not. atomsign(i) )then
                !we have found a "dangling" S--S bond; saturate:
                call xyzchangeS(coord(1,i),coord(2,i),coord(3,i),  &
                    coord(1,kk),coord(2,kk),coord(3,kk),x,y,z)
                iqm = iqm + 1
                call addH( iqm, x, y, z)
              endif
            enddo
          endif
        enddo
        close(31)

        call handle_external_charges()
        call finish_program_files( iqm, iqmprot )
        call external_minimizer( cfrag, iqm, kuser )

        write(0,'(a,i4)') '    done with residue ', kuser

      enddo  ! big loop over residues

end program afnmr_x

subroutine xyzchange(xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew)

        implicit none
        double precision xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew,grad

         grad=dsqrt(1.09d0**2/((xold-xzero)**2+(yold-yzero)**2   &
           +(zold-zzero)**2))
         xnew=xzero+grad*(xold-xzero)
         ynew=yzero+grad*(yold-yzero)
         znew=zzero+grad*(zold-zzero)
         return
end subroutine xyzchange

subroutine xyzchangeS(xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew)

        implicit none
        double precision xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew,grad

         grad=dsqrt(1.33d0**2/((xold-xzero)**2+(yold-yzero)**2   &
           +(zold-zzero)**2))
         xnew=xzero+grad*(xold-xzero)
         ynew=yzero+grad*(yold-yzero)
         znew=zzero+grad*(zold-zzero)
         return
end subroutine xyzchangeS

subroutine xyzchangeO(xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew)

        implicit none
        double precision xold,yold,zold,xzero,yzero,zzero,xnew,ynew,znew,grad

         grad=dsqrt(0.96d0**2/((xold-xzero)**2+(yold-yzero)**2  &
           +(zold-zzero)**2))
         xnew=xzero+grad*(xold-xzero)
         ynew=yzero+grad*(yold-yzero)
         znew=zzero+grad*(zold-zzero)
         return
end subroutine xyzchangeO

subroutine get_atom_range( kstart, kfinal, ktemp,  &
           selectC, restype, ter )
        implicit none

        integer, intent(in)   :: ktemp, selectC(0:*)
        integer, intent(out)  :: kstart, kfinal
        logical, intent(in)   :: ter(0:*)
        character(len=1), intent(in) ::  restype

        if( restype.eq.'P' ) then
          kstart=selectC(ktemp-1)
          kfinal=selectC(ktemp)-1
          if( ter(ktemp) )then
            kfinal=selectC(ktemp)+2
          endif
          if( ter(ktemp-1) ) then
            kstart=selectC(ktemp-1)+3
          endif
        else if( restype.eq.'N' ) then
          kstart=selectC(ktemp-1)
          kfinal=selectC(ktemp)-1
          if( ter(ktemp) )then
            kfinal=selectC(ktemp)+1
          endif
          if( ter(ktemp-1) ) then
            kstart=selectC(ktemp-1)+2
          endif
        else    !  general (G/W) residue type: should be ligand or water
          kstart=selectC(ktemp)
          kfinal=selectC(ktemp+1)-1
        endif

        return
end subroutine get_atom_range

subroutine addatom( kk, iqm, principal )

      use comafnmr
      implicit none
      integer, intent(in) ::  kk,iqm
      logical, intent(in) :: principal
      integer :: j, atno
      character(len=3) ::  i_char
      character(len=1) ::  elem

      if ( demon .or. jaguar ) then
        write( i_char, '(i3)' ) iqm
        dlabel(iqm) = trim(element(kk)) // adjustl(i_char)
        if( len_trim(element(kk)) == 1 ) dlabel(iqm)(5:5) = ' ' 
        write(30,'(a,2x,3f12.5)') dlabel(iqm),(coord(j,kk),j=1,3)
      else if ( terachem ) then
        write(34,'(a2,4x,3f10.4)')element(kk),(coord(j,kk),j=1,3)
      else if ( gaussian .or. orca ) then
        write(30,'(a2,4x,3f10.4)')element(kk),(coord(j,kk),j=1,3)
        dlabel(iqm) = adjustl(element(kk))
        if( orca .and. basis .eq. 'M' .and. principal) then
          if( spinspin ) then
             write(30,'(a)') 'NewGTO "pcJ-2" end;'
          else
             write(30,'(a)') 'NewGTO "pcSseg-2" end;'
          end if
        end if
        if( gaussian .and. basis .eq. 'M' ) then
           if( principal ) then
              if( spinspin ) then
                 atombasis(iqm) = '/basis/pcJ-2/'
              else
                 atombasis(iqm) = '/basis/pcSseg-2/'
              end if
           else
              atombasis(iqm) = '/basis/pcSseg-1/'
           end if
           write(6,*) 'atombasis: ', iqm, trim(atombasis(iqm)), dlabel(iqm)
        end if
      else if ( sqm ) then
        elem = element(kk)(2:2)
        if ( elem == 'H' ) atno = 1
        if ( elem == 'C' ) atno = 6
        if ( elem == 'N' ) atno = 7
        if ( elem == 'O' ) atno = 8
        if ( elem == 'F' ) atno = 9
        if ( elem == 'P' ) atno = 15
        if ( elem == 'S' ) atno = 16
        write(30,'(i2,2x,a2,4x,3f10.4)') atno, element(kk),(coord(j,kk),j=1,3)
      else
        write(30,'(a2,4x,3f10.4)')element(kk),(coord(j,kk),j=1,3)
        dlabel(iqm) = trim(element(kk))
      endif
      if( xtb .or. quick ) then
        write(44,'(a2,4x,3f10.4)')element(kk),(coord(j,kk),j=1,3)
      endif

      if( nofrag ) then
         write(31,'(a,i5,1x,a4,1x,a3,i6,4x,3f8.3,f8.4,f8.3,6x,a2)') 'ATOM  ', &
           kk,atomname_uniq(kk),residue(kk),resno_user(kk), &
           (coord(j,kk),j=1,3), qmcharge(kk),rad(kk),element(kk)
      else
         write(31,'(a,i5,1x,a4,1x,a3,i6,4x,3f8.3,f8.4,f8.3,6x,a2)') 'ATOM  ', &
           kk,atomname(kk),residue(kk),resno_user(kk),(coord(j,kk),j=1,3),   &
           qmcharge(kk),rad(kk),element(kk)
       end if

      return
end subroutine addatom

