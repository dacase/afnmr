          if (qopt) then
            if( nhighatom .lt. iqmprot ) then
               write(30,'(a,i0,a,i0,a,i0)') 'noatoms  atoms=1-', nhighatom, &
                    ', ', iqmprot+1, '-', natom
            else
               write(30,'(a,i0)') 'noatoms  atoms=1-', nhighatom
            end if
            write(30,*)
          endif

          if( spinspin .or. basis .eq. 'M' ) then
             write(30,'(a,i0)') ' atoms=1-', nhighatom
             write(30,*)
          end if

        else if ( sqm ) then
          write(30,'(a)') '#END'
        else if ( jaguar ) then
          write(30,'(a)') '&'
        else if ( demon ) then
          write(30,'(a)') 'END'
          if( nhighatom == 0 ) nhighatom = 4
          if( basis .eq. 'M' )then
            write(30,'(a)') 'BASIS  (DZVP)'
            do i=1,nhighatom
              write(30,'(a,a)') dlabel(i),' (pcSseg-2)'
            end do
            write(30,'(a)') 'AUXIS  (A2)'
            do i=1,nhighatom
              write(30,'(a,a)') dlabel(i),' (GEN-A2*)'
            end do
          else if (basis .eq. 'Q' ) then
            write(30,'(a)') 'BASIS  (pcSseg-2)'
            write(30,'(a)') 'AUXIS  (GEN-A2*)'
          else if (basis .eq. 'T' ) then
            write(30,'(a)') 'BASIS  (pcSseg-1)'
            write(30,'(a)') 'AUXIS  (GEN-A2*)'
          else if (basis .eq. 'D' ) then
            write(30,'(a)') 'BASIS  (pcSseg-0)'
            write(30,'(a)') 'AUXIS  (GEN-A2*)'
          else
            write(0,*) 'Bad basis for demon: ', basis
            stop 1
          endif

          if( demon5 ) then
            write(30,'(a)') 'NMR READ SHIELDING'
          else
            write(30,'(a)') 'NMR READ'
          endif
          do i = 1,nhighatom
            write(30,'(a,a)', advance='no' ) dlabel(i),' '
            if( mod(i,12).eq.0 .and. i.ne.nhighatom ) write(30,'(a)') ' &'
          end do
          write(30,'(a)') ' '
          write(30,'(a)') 'PRINT NMR'

          if( qopt ) then
            write(30,'(a)') 'OPTIMIZE CARTESIAN MAX=5'
            if( nhighatom .lt. iqmprot ) then
               write(30,'(a)') 'CONSTANTS'
               do i=nhighatom+1,iqmprot
                  write(30,'(a,a)') dlabel(i), '  XYZ'
               end do
            endif
          endif

        else if( orca ) then
          write(30,'(a)') '*'
          if( qopt ) then
            write(30,'(a)') '%geom MaxIter=10'
            if( nhighatom .lt. iqmprot ) then
               ! constrain protein/nuc. acid atoms not in the primary residue
               write(30,'(a)') '      Constraints'
               write(30,'(a,i0,a,i0,a)') &
                  '        { C ', nhighatom, ':', iqmprot-1, ' C }'

                write(30,'(a)') '      end'
                write(30,'(a)') '    end'
            endif
          endif
          write(30,'(a)') '%eprnmr  ori GIAO'
          write(30,'(a)', advance='no') '   nuclei = 1'
          do i=2,nhighatom
             write(30,'(a1,i3)', advance='no') ',', i
          end do
          write(30,'(a)') ' {shift}'
          write(30,'(a)') 'end'

        else if ( jaguar ) then
          write(30,'(a)') '&'

        else if ( qchem ) then
          write(30,'(a)') '$end'
          write(30,'(a)') '   '
          write(30,'(a)') '$rem'
          write(30,'(a)') 'MOPROP        1'
          write(30,'(a)') 'SCF_ALGORITHM DIIS'
          if( functional(1:4) == 'OLYP' ) then
             write(30,'(a)') 'EXCHANGE      OPTX'
             write(30,'(a)') 'CORRELATION   LYP'
          else if( functional(1:5) == 'O3LYP' ) then
